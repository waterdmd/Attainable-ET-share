# FIG 4 — Targeting curve
# ══════════════════════════════════════════════════════════════════════════
print("Fig 4 (targeting curve)...")
YR_PAL = ['#bdd7e7','#6baed6','#2171b5','#084594','#08306b']

fig, ax = plt.subplots(figsize=(7, 5.5))
for i, yr in enumerate(years):
    d = df[df.Year==yr].copy()
    d = d[d['VolD_AF']>0].sort_values('VolD_AF', ascending=False)
    d['cum_vol']  = d['VolD_AF'].cumsum()
    d['cum_area'] = d['Area_ha'].cumsum()
    tv, ta = d['VolD_AF'].sum(), d['Area_ha'].sum()
    ax.plot(d['cum_area']/ta*100, d['cum_vol']/tv*100,
            color=YR_PAL[i], linewidth=1.3, alpha=0.85, label=str(yr))

# Pooled (farm-average across years)
pooled = df.groupby('CSBID').agg(Area_ha=('Area_ha','mean'),
                                  VolD_AF=('VolD_AF','mean')).reset_index()
pooled = pooled[pooled['VolD_AF']>0].sort_values('VolD_AF', ascending=False)
pooled['cum_vol']  = pooled['VolD_AF'].cumsum()
pooled['cum_area'] = pooled['Area_ha'].cumsum()
tv_p, ta_p = pooled['VolD_AF'].sum(), pooled['Area_ha'].sum()
p_pct_area = pooled['cum_area']/ta_p*100
p_pct_vol  = pooled['cum_vol'] /tv_p*100
ax.plot(p_pct_area, p_pct_vol, color='#08306b', linewidth=2.8,
        label='Pooled 2020–2024', zorder=5)

# Reference lines and annotation
