# FIG 1 — Statistical conditioning of peer cohorts
#   (a) Distribution of benchmarked fields by cohort sample size
#   (b) Width of 95% CI for 5th-pct ET as a function of cohort size
# ══════════════════════════════════════════════════════════════════════════
print("Fig 1 (statistical conditioning of peer cohorts)...")

BIN_ORDER = ['<5', '5-9', '10-19', '20-29', '30-49', '50-99', '100-199', '200+']

bin_overall = pd.read_csv(os.path.join(DATA_DIR, 'cohort/cohort_size_bins_overall.csv'))
ci_df = pd.read_csv(os.path.join(DATA_DIR, 'cohort/cohort_p5_ci_summary_by_bin.csv'))

bin_overall['size_bin'] = pd.Categorical(bin_overall['size_bin'], BIN_ORDER, ordered=True)
bin_overall = bin_overall.sort_values('size_bin')

ci_df['size_bin'] = pd.Categorical(ci_df['size_bin'], BIN_ORDER, ordered=True)
ci_stats = (
    ci_df.groupby('size_bin', as_index=False)
         .agg(median_ci_width=('median_ci_width', 'median'),
              q25=('median_ci_width', lambda x: x.quantile(0.25)),
              q75=('median_ci_width', lambda x: x.quantile(0.75)))
         .sort_values('size_bin')
)

fig1, axes1 = plt.subplots(1, 2, figsize=(11, 4.2))

# Panel (a): cohort size distribution
ax1a = axes1[0]
ax1a.bar(bin_overall['size_bin'], bin_overall['pct_fields'],
         color='#4D4D4D', edgecolor='none', width=0.55)
ax1a.set_ylabel('Fields (%)', fontsize=11)
ax1a.set_xlabel('Cohort size (N)', fontsize=11, labelpad=2)
ax1a.set_ylim(0, min(100, bin_overall['pct_fields'].max() * 1.20))
ax1a.grid(axis='y', color='#bbbbbb', alpha=0.3, linewidth=0.5)
ax1a.set_axisbelow(True)
ax1a.set_facecolor('white')
for side in ('top', 'right'):
    ax1a.spines[side].set_visible(False)
ax1a.tick_params(axis='both', length=2.5, labelsize=9)

# Panel (b): CI width vs cohort size
ax1b = axes1[1]
lc = '#1F77B4'
ax1b.plot(ci_stats['size_bin'], ci_stats['median_ci_width'],
          color=lc, linewidth=2.4, marker='o', markersize=4)
ax1b.fill_between(ci_stats['size_bin'], ci_stats['q25'], ci_stats['q75'],
                  color=lc, alpha=0.15)
ax1b.set_ylabel('Width of 95% CI for P5 ET (mm)', fontsize=11)
ax1b.set_xlabel('Cohort size (N)', fontsize=11, labelpad=2)
ax1b.grid(axis='y', color='#bbbbbb', alpha=0.3, linewidth=0.5)
ax1b.set_axisbelow(True)
ax1b.set_facecolor('white')
for side in ('top', 'right'):
    ax1b.spines[side].set_visible(False)
ax1b.tick_params(axis='both', length=2.5, labelsize=9)

plt.tight_layout()
savefig_both(fig1, 'Fig1_cohort_conditioning')
plt.close()
