def draw_violin_box(ax, data_list, labels, colors, ylim_top=950):
    vp = ax.violinplot(data_list, positions=range(len(data_list)),
                       showmedians=False, showextrema=False, widths=0.72)
    for body, col in zip(vp['bodies'], colors):
        body.set_facecolor(col); body.set_alpha(0.60); body.set_edgecolor('none')

    for i, data in enumerate(data_list):
        q1, med, q3 = np.percentile(data, [25, 50, 75])
        p10, p90    = np.percentile(data, [10, 90])
        # whiskers P10–P90
        ax.vlines(i, p10, p90, color='black', linewidth=1.2, zorder=3)
        ax.hlines(p10, i-0.08, i+0.08, color='black', linewidth=1.2, zorder=3)
        ax.hlines(p90, i-0.08, i+0.08, color='black', linewidth=1.2, zorder=3)
        # IQR box
        rect = mpatches.Rectangle((i-0.15, q1), 0.30, q3-q1,
                                   facecolor='white', edgecolor='black',
                                   linewidth=1.2, zorder=4)
        ax.add_patch(rect)
        ax.hlines(med, i-0.15, i+0.15, color='black', linewidth=2.0, zorder=5)

    ax.set_xticks(range(len(labels)))
    ax.set_xticklabels(labels, fontsize=10)
    ax.set_ylim(0, ylim_top)
    ax.set_xlim(-0.6, len(labels)-0.4)
    ax.text(0.98, 0.97,
            'Box: IQR  |  Line: median\nWhiskers: 10th–90th percentile',
            transform=ax.transAxes, fontsize=8, va='top', ha='right',
            bbox=dict(boxstyle='round,pad=0.3', fc='white', ec='#cccccc', alpha=0.9))

