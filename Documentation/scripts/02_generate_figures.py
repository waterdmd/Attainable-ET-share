    pcts = by_cat / tot * 100
    cum  = pcts.cumsum().values
    cats = pcts.index.tolist()
    x    = np.arange(len(cats))

    # Use lighter color for "Other" bar
    colors = ['#4C9BE8'] * len(cats)
    if 'Other' in cats:
        colors[cats.index('Other')] = '#B0B0B0'

    bars = ax.bar(x, pcts.values, color=colors, edgecolor='white',
                  linewidth=0.5, width=0.65)

    ax2 = ax.twinx()
    ax2.plot(x, cum, 'o-', color='#1a1a2e', linewidth=1.6, markersize=5, zorder=5)
    ax2.set_ylim(0, 108)
    ax2.set_ylabel('Cumulative share (%)', fontsize=10)
    ax2.yaxis.set_major_formatter(mticker.FormatStrFormatter('%g%%'))
    ax2.tick_params(axis='y', labelsize=9)
    ax2.grid(False)

    ax.set_xticks(x)
    ax.set_xticklabels(cats, rotation=0, ha='center', fontsize=9.0)
    ax.set_ylabel('Share of total reduction potential (%)', fontsize=10)
    ax.set_xlabel(xlabel, fontsize=11, labelpad=4)
    ax.yaxis.set_major_formatter(mticker.FormatStrFormatter('%g%%'))
    ax.set_ylim(0, pcts.max() * 1.32)
    ax.grid(False)
